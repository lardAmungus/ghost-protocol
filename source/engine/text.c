/* Font color palette index (1 = white text on transparent bg) */
#define FONT_COLOR 1

/* Flag: has text system been initialized? */
static int text_ready = 0;

/*
 * Expand a 1bpp glyph (8 bytes) into a 4bpp tile (32 bytes).
 * Set pixels use palette index FONT_COLOR; clear pixels use 0 (transparent).
 */
static void expand_1bpp_to_4bpp(const u8* src_1bpp, u32* dst_4bpp) {
    for (int row = 0; row < 8; row++) {
        u32 line = 0;
        u8 bits = src_1bpp[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                line |= (u32)FONT_COLOR << (col * 4);
            }
        }
        dst_4bpp[row] = line;
    }
}

/*
 * Expand a 1bpp glyph into a 4bpp tile with opaque background for dialogue box.
 * Set pixels use palette index 1 (text color); clear pixels use index 2 (fill color).
 */
#define DBOX_TEXT_COLOR 1
#define DBOX_BG_COLOR  2
static void expand_1bpp_to_4bpp_dbox(const u8* src_1bpp, u32* dst_4bpp) {
    for (int row = 0; row < 8; row++) {
        u32 line = 0;
        u8 bits = src_1bpp[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                line |= (u32)DBOX_TEXT_COLOR << (col * 4);
            } else {
                line |= (u32)DBOX_BG_COLOR << (col * 4);
            }
        }
        dst_4bpp[row] = line;
    }
}

void text_init(void) {
    /* Clear BG0 screenblock (use 16-bit writes for VRAM) */
    memset16(TEXT_MAP, 0, 1024);

    /* Expand all font glyphs from 1bpp to 4bpp into VRAM CBB 0 */
    for (int i = 0; i < FONT_CHAR_COUNT; i++) {
        expand_1bpp_to_4bpp(
            &font_1bpp[i * 8],
            (u32*)&tile_mem[TEXT_CBB][FONT_TILE_BASE + i]
        );
    }

    /* Generate dialogue box font with opaque background at tiles 128-222 */
    for (int i = 0; i < FONT_CHAR_COUNT; i++) {
        expand_1bpp_to_4bpp_dbox(
            &font_1bpp[i * 8],
            (u32*)&tile_mem[TEXT_CBB][DBOX_FONT_BASE + i]
        );
    }

    /* Set palette: index 0 = transparent (magenta/black), index 1 = white */
    pal_bg_mem[0] = RGB15(0, 0, 0);
    pal_bg_mem[1] = RGB15(31, 31, 31);

    text_ready = 1;
}

void text_print(int tx, int ty, const char* str) {
    if (!text_ready || !str) return;

    int x = tx;
    while (*str) {
        if (*str == '\n') {
            ty++;
            x = tx;
        } else {
            if (x >= 0 && x < 30 && ty >= 0 && ty < 20) {
                int ch = *str - ' ';
                if (ch < 0 || ch >= FONT_CHAR_COUNT) ch = 0;
                TEXT_MAP[ty * 32 + x] = (u16)(FONT_TILE_BASE + ch);
            }
            x++;
        }
        str++;
    }
}

/* Print a decimal number at tile position */
void text_print_int(int tx, int ty, int value) {
    char buf[12];
    int pos = 0;
    int neg = 0;

    if (value < 0) {
        neg = 1;
        /* Avoid UB on INT_MIN: cast to unsigned for negation */
        value = (int)(-(unsigned)value);
    }
    if (value == 0) {
        buf[pos++] = '0';
    } else {
        while (value > 0) {
            buf[pos++] = (char)('0' + value % 10);
            value /= 10;
        }
    }
    if (neg) buf[pos++] = '-';

    /* Reverse and print */
    int x = tx;
    for (int i = pos - 1; i >= 0; i--) {
        if (x >= 0 && x < 30 && ty >= 0 && ty < 20) {
            int ch = buf[i] - ' ';
            TEXT_MAP[ty * 32 + x] = (u16)(FONT_TILE_BASE + ch);
        }
        x++;
    }
}

void text_clear_rect(int tx, int ty, int w, int h) {
    if (!text_ready) return;
    /* Clamp to valid range to prevent negative index corruption */
    if (tx < 0) { w += tx; tx = 0; }
    if (ty < 0) { h += ty; ty = 0; }
    if (w <= 0 || h <= 0) return;
    if (tx + w > 30) w = 30 - tx;
    if (ty + h > 20) h = 20 - ty;
    for (int row = ty; row < ty + h; row++) {
        memset16(&TEXT_MAP[row * 32 + tx], 0, (u32)w);
    }
}

void text_clear_all(void) {
    if (!text_ready) return;
    memset16(TEXT_MAP, 0, 1024);
}

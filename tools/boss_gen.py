for bi, (bname, frames) in enumerate(bosses):
    print(f"static const u32 spr_boss_{bname}[2][16][8] = {{")
    for fi, frame in enumerate(frames):
        # Validate and pad rows if needed
        while len(frame) < 32:
            frame.append("0" * 32)
        for ri in range(32):
            clean = frame[ri].replace(' ', '').replace('.', '0')
            if len(clean) < 32:
                frame[ri] = clean + '0' * (32 - len(clean))
            elif len(clean) > 32:
                frame[ri] = clean[:32]
        tiles = grid32_to_tiles(frame)
        print(f"  {{ /* frame {fi} */")
        for ti, tile in enumerate(tiles):
            print(f"    {tile_str(tile)},")
        print(f"  }},")
    print(f"}};")
    print()


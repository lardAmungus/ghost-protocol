"""Ghost Protocol — Boss Sprite Generator (32x32, 5 types x 2 frames)"""

def row_to_u32(pixels):
    v = 0
    for i, p in enumerate(pixels):
        v |= (p & 0xF) << (i * 4)
    return v

def parse_row(s):
    s = s.replace(' ', '').replace('.', '0')
    return [int(c, 16) for c in s]

def grid32_to_tiles(grid):
    """32x32 grid -> 16 tiles in row-major order."""
    tiles = [[] for _ in range(16)]
    for y in range(32):
        px = parse_row(grid[y])
        assert len(px) == 32, f"Row {y}: got {len(px)} pixels"
        tr = y // 8
        for tc in range(4):
            chunk = px[tc*8:(tc+1)*8]
            tiles[tr*4 + tc].append(row_to_u32(chunk))
    return tiles

def fmt(v): return f"0x{v:08X}"
def tile_str(t): return "{ " + ", ".join(fmt(v) for v in t) + " }"


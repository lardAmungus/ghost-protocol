#!/usr/bin/env python3
"""Ghost Protocol -- Boss Sprite Generator (32x32, 6 bosses x 2 frames)

Generates C-formatted 4bpp tile arrays for all 6 boss sprites.
Each boss is 32x32 pixels = 16 tiles (4x4 grid of 8x8 tiles).
Two frames per boss: idle (frame 0) and attack pulse (frame 1).

Palette index convention:
  0 = transparent
  1 = dark outline (near-black)
  2 = dark body shade
  3 = medium body
  4 = light body / highlight
  5 = accent dim (eye/core dark)
  6 = accent mid (eye/core)
  7 = accent bright (core glow) -- BRIGHTEST
  8 = secondary color dark
  9 = secondary mid
  A = secondary light
  B = tech accent dark
  C = tech accent bright
  D = AA edge / sub-outline
  E = detail
  F = white / flash
"""

# ============================================================
# Tile conversion functions (from sprite_gen.py)
# ============================================================

def row_to_u32(pixels):
    """Convert 8 pixels (ints 0-15) to GBA 4bpp u32. LSN = leftmost."""
    v = 0
    for i, p in enumerate(pixels):
        v |= (p & 0xF) << (i * 4)
    return v

def parse_row(s):
    """Parse a hex string row like '00113300' into list of ints."""
    s = s.replace(' ', '').replace('.', '0')
    return [int(c, 16) for c in s]

def grid32_to_tiles(grid):
    """32x32 grid -> 16 tiles in row-major order, each = list of 8 u32."""
    tiles = [[] for _ in range(16)]
    for y in range(32):
        px = parse_row(grid[y])
        if len(px) != 32:
            raise ValueError(f"Row {y}: expected 32 pixels, got {len(px)}")
        tr = y // 8   # tile row 0-3
        for tc in range(4):
            chunk = px[tc*8:(tc+1)*8]
            tiles[tr*4 + tc].append(row_to_u32(chunk))
    return tiles

def fmt(val):
    return f"0x{val:08X}"

def tile_str(tile):
    return "{ " + ", ".join(fmt(v) for v in tile) + " }"

# ============================================================
# Validation helper
# ============================================================

def validate_and_fix_boss(name, frames):
    """Validate all frames have exactly 32 rows of 32 chars. Auto-fix lengths."""
    for fi, frame in enumerate(frames):
        while len(frame) < 32:
            frame.append("0" * 32)
        if len(frame) > 32:
            frames[fi] = frame[:32]
            frame = frames[fi]
        for ri in range(32):
            clean = frame[ri].replace(' ', '').replace('.', '0')
            if len(clean) < 32:
                frame[ri] = clean + '0' * (32 - len(clean))
            elif len(clean) > 32:
                frame[ri] = clean[:32]
            else:
                frame[ri] = clean



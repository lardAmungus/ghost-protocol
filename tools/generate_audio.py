# 2 channels only: ch0 = melody (smp 1), ch1 = bass (smp 2).
# Both use the same 32-sample looping sine wave.
# No drums ÔÇö drum synthesis sounds like static on GBA hardware.

def sine32():
    """32-sample looping sine wave, 8-bit signed PCM."""
    return bytes((int(118 * math.sin(2 * math.pi * i / 32)) & 0xFF)
                 for i in range(32))


def smp_hdr(name, data, vol=56):
    words = len(data) // 2
    return struct.pack('>22sHBBHH',
                       name.encode()[:22].ljust(22, b'\0'),
                       words, 0, vol, 0, words)  # loop_start=0, loop_len=words


def note_cell(note_idx, smp, effect=0, param=0):
    period = PERIODS[note_idx] if 0 <= note_idx < len(PERIODS) else 0
    b1 = ((smp >> 4) << 4) | ((period >> 8) & 0xF)
    b2 = period & 0xFF
    b3 = ((smp & 0xF) << 4) | (effect & 0xF)
    b4 = param & 0xFF
    return bytes([b1, b2, b3, b4])


def build_pattern(cells):
    """cells: [(row, ch, note_idx, smp, effect, param), ...]"""
    pat = bytearray(MOD_ROWS * MOD_CHAN * 4)
    for row, ch, note, smp, fx, param in cells:
        if 0 <= row < MOD_ROWS and 0 <= ch < MOD_CHAN:
            off = (row * MOD_CHAN + ch) * 4
            pat[off:off+4] = note_cell(note, smp, fx, param)
    return bytes(pat)


def build_mod(title, patterns, order, speed=6, bpm=90):
    """Assemble a 2-instrument MOD file (both use the same 32-byte sine)."""
    sd = sine32()
    title_b = title.encode()[:20].ljust(20, b'\0')
    hdrs  = smp_hdr('Melody', sd, 56)
    hdrs += smp_hdr('Bass',   sd, 48)
    for _ in range(29):
        hdrs += struct.pack('>22sHBBHH', b'\0'*22, 0, 0, 0, 0, 0)

    # Inject BPM+speed into row 0 channels 0/1 of first pattern
    if patterns:
        p0 = bytearray(patterns[0])
        p0[0]=0x00; p0[1]=0x00; p0[2]=0x0F; p0[3]=bpm & 0xFF
        p0[4]=0x00; p0[5]=0x00; p0[6]=0x0F; p0[7]=speed & 0x1F
        patterns[0] = bytes(p0)

    n_pat = max(order) + 1 if order else 1
    mod   = title_b + hdrs
    mod  += struct.pack('BB', len(order), 0)
    mod  += bytes(order + [0] * (128 - len(order)))
    mod  += b'M.K.'
    for i in range(n_pat):
        mod += patterns[i] if i < len(patterns) else bytes(MOD_ROWS * MOD_CHAN * 4)
    mod += sd + sd   # sample data for instrument 1 and 2
    return mod


def note(row, ch, n, smp, vol=48):
    """Convenience: returns a pattern cell tuple."""
    return (row, ch, n, smp, VC, vol)


S = 1   # melody instrument number
B = 2   # bass instrument number

# ÔöÇÔöÇÔöÇ Track builders ÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇ
# Melody rows start at 4 (row 0 is reserved for BPM injection).
# Notes every 4 rows at speed=6, bpm=90 ÔåÆ ~1.8 notes/sec (moderate pace).

def _track(title, mel, bass, bpm=90, speed=6, loops=4):
    cells = []
    for row, n, vol in mel:
        if row > 0:
            cells.append(note(row, 0, n, S, vol))
    for row, n, vol in bass:
        if row > 0:
            cells.append(note(row, 1, n, B, vol))
    p = build_pattern(cells)
    return build_mod(title, [p], [0] * loops, speed, bpm)



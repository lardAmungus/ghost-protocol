def sox_wav(outpath, *args):
    """Run sox to generate a 16384 Hz / 8-bit unsigned / mono WAV."""
    cmd = ['sox', '-n',
           '-r', '16384', '-b', '8', '-e', 'unsigned-integer', '-c', '1',
           outpath] + [str(a) for a in args]
    subprocess.run(cmd, check=True, capture_output=True)


